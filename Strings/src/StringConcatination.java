
public class StringConcatination {

	public static void main(String[] args) {
		String s1  = "ram";
		String s2 = "sita";
		System.out.println("s1 ="+ s1);
		System.out.println("s2 ="+ s2);
		
		String  s3 = s1.concat(s2);
		System.out.println("s1 ="+ s3);
		System.out.println("s2 ="+ s2);
	}

}
